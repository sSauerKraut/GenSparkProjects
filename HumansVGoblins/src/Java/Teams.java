public class Teams {

    int activeCombatants;
    int gold;
    int goldMultiplier;
    int landCount;

    public void setActiveCombatants(int x) {this.activeCombatants = x;}
    public void setGold(int x) {this.gold = x;}
    public void setGoldMultiplier(int x) {this.goldMultiplier = x;}
    public void setLandCount(int x) {this.landCount = x;}

    public int getActiveCombatants() {return this.activeCombatants;}
    public int getGold() {return this.gold;}
    public int getGoldMultiplier() {return this.goldMultiplier;}
    public int getLandCount() {return this.landCount;}
}
